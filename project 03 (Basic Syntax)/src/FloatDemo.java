public class FloatDemo {
    public static void main(String[] args) {
        System.out.println(9.5);

        //声明 double 类型的价格变量
        double price = 9.5;
        System.out.println("汽油的价格是："+ price + " 元");

        //声明 float 类型的变量
        float height = 176.5F;
        System.out.println(height);

        double d1 = 0.1;
        double d2 = 0.2;
        System.out.println(d1 + d2);
    }
}
